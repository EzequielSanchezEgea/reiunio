package com.ezequiel.reiunio.enums;

/**
 * Enum representing the types of actions that can be performed.
 */
public enum ActionType {
    
    /**
     * Represents the creation of an entity.
     */
    CREATION,
    
    /**
     * Represents the modification of an entity.
     */
    MODIFICATION,
    
    /**
     * Represents the deletion of an entity.
     */
    DELETION
}
