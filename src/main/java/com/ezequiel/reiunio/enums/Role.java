package com.ezequiel.reiunio.enums;

/**
 * Enum representing user roles in the application.
 */
public enum Role {

    /**
     * Administrator role with full access to all system features.
     */
    ADMIN,

    /**
     * Basic user role with limited access and permissions.
     */
    BASIC_USER,

    /**
     * Extended user role with additional privileges compared to a basic user.
     */
    EXTENDED_USER
}
