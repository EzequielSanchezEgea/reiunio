package com.ezequiel.reiunio.enums;

/**
 * Enum representing the physical or functional condition of a game.
 */
public enum GameState {

    /**
     * The game is brand new and unused.
     */
    NEW,

    /**
     * The game is used but in very good condition.
     */
    GOOD,

    /**
     * The game shows signs of wear but is still functional.
     */
    ACCEPTABLE,

    /**
     * The game is damaged and may not be fully functional.
     */
    DAMAGED
}
